package PARK.Step_1;

import java.io.BufferedWriter;
import java.io.OutputStreamWriter;

public class Main12 {

  public static void main(String[] args) throws Exception{

    BufferedWriter wr = new BufferedWriter(new OutputStreamWriter(System.out));
    wr.write("\\    /\\");
    wr.newLine();
    wr.write(" )  ( ')");
    wr.newLine();
    wr.write("(  /  )");
    wr.newLine();
    wr.write(" \\(__)|");
    wr.close();
  }
}
